import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const dist = join(root, 'lab/dist');
let html = await readFile(join(dist, 'index.html'), 'utf8');

html = await inlineCss(html);
html = await inlineJs(html);
html = html.replace(/\s+crossorigin(=""|)/g, '');

const out = `// Auto-generated by scripts/generate-lab-html.mjs. Do not edit by hand.\nexport const STREAMING_LAB_HTML = ${JSON.stringify(html)};\n`;
await writeFile(join(root, 'src/frontend.generated.ts'), out);

async function inlineCss(input) {
  return await replaceAsync(input, /<link rel="stylesheet" crossorigin href="([^"]+)">/g, async (_match, href) => {
    const css = await readFile(join(dist, href.replace(/^\//, '')), 'utf8');
    return `<style>${css}</style>`;
  });
}

async function inlineJs(input) {
  return await replaceAsync(input, /<script type="module" crossorigin src="([^"]+)"><\/script>/g, async (_match, src) => {
    const js = await readFile(join(dist, src.replace(/^\//, '')), 'utf8');
    const encoded = Buffer.from(js, 'utf8').toString('base64');
    const bootstrap = `const source = decodeURIComponent(escape(atob(${JSON.stringify(encoded)})));\nawait import(URL.createObjectURL(new Blob([source], { type: 'text/javascript' })));`;
    return `<script type="module">${bootstrap}</script>`;
  });
}

async function replaceAsync(str, regex, asyncFn) {
  const matches = [...str.matchAll(regex)];
  let next = str;
  for (const match of matches) {
    next = next.replace(match[0], await asyncFn(...match));
  }
  return next;
}
